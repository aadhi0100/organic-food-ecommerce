const fs = require('fs')
const path = require('path')

function main() {
  const repoRoot = path.join(__dirname, '..')
  const imagesDir = path.join(repoRoot, 'public', 'images', 'products')
  const outFile = path.join(repoRoot, 'src', 'lib', 'productImages.generated.ts')

  const exts = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif'])
  const files = fs
    .readdirSync(imagesDir)
    .filter((f) => exts.has(path.extname(f).toLowerCase()))
    .sort((a, b) => a.localeCompare(b))

  const lines = []
  lines.push('// This file is auto-generated by scripts/generate-product-images-map.js')
  lines.push('// Do not edit by hand.')
  lines.push('')
  lines.push('export const PRODUCT_IMAGE_PATHS = [')
  for (const f of files) {
    // Use public/ path (no "public" prefix) for Next.js static assets.
    lines.push(`  ${JSON.stringify(`/images/products/${f}`)},`)
  }
  lines.push('] as const')
  lines.push('')

  fs.writeFileSync(outFile, lines.join('\n'), 'utf8')
  // eslint-disable-next-line no-console
  console.log(`Wrote ${path.relative(repoRoot, outFile)} (${files.length} images)`)
}

main()

